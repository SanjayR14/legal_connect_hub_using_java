    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        // TODO add your handling code here:
        String enteredUsername = Usernmae.getText();
        String enteredPassword = new String(jPasswordField1.getPassword());


        try {
            // Establish a connection to your database (replace the connection URL, username, and password)
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/java_project", "root", "0000");

            // Create a query to check if the entered credentials exist in the database
            String query = "SELECT * FROM signup WHERE username = ? AND password = ?";
            PreparedStatement pstmt = conn.prepareStatement(query);
            pstmt.setString(1, enteredUsername);
            pstmt.setString(2, enteredPassword);

            ResultSet rs = pstmt.executeQuery();

        if (rs.next()) {
            // Check if the password matches
            String storedPassword = rs.getString("password");
            if (enteredPassword.equals(storedPassword)) {
                // Valid credentials, login successful
                JOptionPane.showMessageDialog(this, "Login Successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
                 
               
                  new Login1(enteredUsername).setVisible(true);
                  this.dispose();
                 
                
            } else {
                // Invalid password, login failed
                JOptionPane.showMessageDialog(this, "Invalid password", "Error", JOptionPane.ERROR_MESSAGE);
                new MainLogin().setVisible(false);
            }
        } else {
            // Invalid username, login failed
            JOptionPane.showMessageDialog(this, "Invalid username", "Error", JOptionPane.ERROR_MESSAGE);
            new MainLogin().setVisible(true);
        }
            

            // Close the result set, prepared statement, and connection
            rs.close();
            pstmt.close();
            conn.close();
        } catch (SQLException ex) {
            // Handle any SQL exceptions
            ex.printStackTrace();
}
        this.dispose();

       

    }//GEN-LAST:event_LoginActionPerformed

